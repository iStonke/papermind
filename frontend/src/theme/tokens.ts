export function getGlassPalette(variant, isDark = true) {
  const resolved = resolvePaperMindColorVariant(variant);
  const table = isDark ? GLASS_PALETTES_DARK : GLASS_PALETTES_LIGHT;
  return table[resolved] || table.slate;
}
